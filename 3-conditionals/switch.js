// Write a switch statement that
// console logs witty statements for each
// day of the standard work week: Monday, Tuesday,
// Wednesday, Thursday, Friday
var today = 'Tuesday';

switch (today) {
  case 'Monday':
    console.log('Better make coffee.');
    break;
  case 'Tuesday':
    console.log('Four days \'til freedom.');
    break;
  case 'Wednesday':
    console.log('Hey Mike. Guess what day it is?');
    break;
  case 'Thursday':
    console.log('One day more!');
    break;
  case 'Friday':
    console.log('If you had a three day weekend, you\'d be home by now!');
    break;
  default:

}
